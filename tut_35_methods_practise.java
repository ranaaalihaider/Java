public class tut_35_methods_practise {
    //Method for Multiplication table
    static void table(int number){
        for(int i=1;i<=10;i++){
            System.out.println(number+" X "+i+" = "+(i*number));
        }
    }
    public static void main(String[] args) {
        //Multiplication table
        table(5);


    }
    
}
