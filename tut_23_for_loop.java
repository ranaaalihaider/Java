public class tut_23_for_loop {
    public static void main(String[] args) {
        //printing natural number in reverse order
        for(int i=20; i>0 ; i--){
            System.out.println(i);
        }
        //printing odd number first 10
        System.out.println("Following are odd Numbers first 1");
        for (int i=0 ; i<=10;i++){
            System.out.println(i+"- "+(2*i+1));
        }
    }
}
