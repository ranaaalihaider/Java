public class Switch_01{
    public static void main (String [] args){
        int a=2;
        switch (a ){
            case 1:
                System.out.println("number is 1");
                
                break;
            case 2:
            System.out.println("Number is 2");
                break;
        
            default:
                System.out.println("Number is not 1 or 2");
                break;
        }
    }
}